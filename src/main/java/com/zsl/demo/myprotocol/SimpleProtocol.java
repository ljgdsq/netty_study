package com.zsl.demo.myprotocol;

public class SimpleProtocol {
    private int length;
    private byte[] data;


    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }


}
